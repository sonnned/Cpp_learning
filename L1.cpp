#include <iostream>

using namespace std;

int main() {
  cout << "Hello, World!" << endl;
  cout << "Bye, World!" << endl;
}

/*
 * Explicación:
 * cout viene de la librería iostream y nos permite imprimir en pantalla.
 * << es un operador que nos permite concatenar cadenas de texto. (interceptor operator)
 * "Hello, World!" es una cadena de texto.
 * 
 * Si queremos imprimir varias líneas, al final de cada línea debemos poner << endl que es un salto de línea.
*/